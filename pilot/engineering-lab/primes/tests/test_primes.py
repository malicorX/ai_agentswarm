def test_primes_script_outputs_first_100() -> None:
    proc = subprocess.run(
        [sys.executable, str(PRIMES_PY)],
        cwd=FIXTURE_ROOT,
        capture_output=True,
        text=True,
        check=False,
    )
    assert proc.returncode == 0, proc.stderr or proc.stdout
    lines = [line.strip() for line in proc.stdout.strip().splitlines() if line.strip()]
    values = [int(line) for line in lines]
    assert values == EXPECTED_FIRST_100
